var a =10;
var b =21;

// //Arithmetic Operators
console.log(a+b); // Addition Operator
console.log(b-a); // Subtraction Operator
console.log(a*b); // Multiplication Operator
console.log(b/a); // Division Operator
console.log(b%a); // Modulus Operator

// Increment/Decrement Operators E.g:
var c = 5;
var d = c++ // Post Increment Operator
console.log(c);

var f = 7;
var e = ++f // Pre Increment Operator
console.log(e);

// Assignment Operators e.g:
var a = 10;
a += 3; // Addition Assignment Operator '+='
a -= 2; // Subtraction Assignment Operator '-='

console.log(a);

